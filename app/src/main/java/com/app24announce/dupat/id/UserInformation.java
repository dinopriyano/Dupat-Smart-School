package com.app24announce.dupat.id;

public class UserInformation {

    public String nama,nis,kelas,email,password,posisi,ketuakelas,fotoprofil,nuptk,gurupiket;

    public UserInformation()
    {

    }


    public UserInformation(String nama, String nis, String kelas, String email, String password, String posisi, String ketuakelas, String fotoprofil, String nuptk, String gurupiket) {
        this.nama = nama;
        this.nis = nis;
        this.kelas = kelas;
        this.email = email;
        this.password = password;
        this.posisi = posisi;
        this.ketuakelas = ketuakelas;
        this.fotoprofil = fotoprofil;
        this.nuptk = nuptk;
        this.gurupiket = gurupiket;
    }
}
